# Now, you will learn how to create a custom module
# It is relatively simple

# First, make a directory
# Second, create an EMPTY python file inside called __init__.py
# This is what allows python to use it as a module
# Stick your files in there
# Now you can
import folder.fileName
# And you get your code!

# Alternatively, you can simply have your main file in the same folder as some support files and then you don't need the
# __init__.py or the folder name in the import

import fileThatIsInThisDirectory

# RULES:
# NEVER USE SPACES IN YOUR FILE NAMES EVER EVER EVER
# Cases you should use:
#   camelCase, UpperCase, NOT LOWERCASE

# And that's all! Very simple!

